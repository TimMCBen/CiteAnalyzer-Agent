def build_report_artifact(
    target_paper: TargetPaper,
    citing_papers: list[CitingPaper],
    author_profiles: list[AuthorProfile],
    scholar_labels: list[ScholarLabel],
    author_summary: AuthorSummary,
    citation_contexts: list[CitationContext],
    sentiment_summary: SentimentSummary,
    fetch_summary: FetchSummary | None = None,
    source_trace: list[SourceTrace] | None = None,
    state_errors: list[str] | None = None,
    author_identity_skipped_papers: list[dict[str, str]] | None = None,
    output_dir: Path | None = None,
    country_resolver: CountryResolverProtocol | None = None,
    title_translator: Callable[[str], str | None] | None = translate_title_to_chinese,
    executive_summary_builder: ExecutiveSummaryBuilder | None = None,
) -> ReportArtifact:
    """Build report files from citation, author, sentiment, and provenance data."""
    report_id = target_paper.canonical_id or target_paper.doi or target_paper.title or "unknown-target"
    safe_report_id = _slugify(report_id)
    report_dir = (output_dir or DEFAULT_REPORT_DIR).resolve() / safe_report_id
    report_dir.mkdir(parents=True, exist_ok=True)

    provenance = _build_provenance(
        fetch_summary,
        source_trace,
        state_errors,
        scholar_labels,
        author_profiles,
        author_identity_skipped_papers,
    )
    institution_distribution = _build_institution_distribution(author_profiles)
    country_distribution, country_trace = _build_country_distribution(author_profiles, country_resolver)
    provenance["country_resolution_trace"] = country_trace
    provenance["report_warnings"] = []
    provenance["pdf_export_status"] = "pending"
    target_title_zh = _translate_target_title(target_paper.title, title_translator, provenance)
    target_arxiv_id = _target_arxiv_id(target_paper)
    target_arxiv_url = f"https://arxiv.org/abs/{target_arxiv_id}" if target_arxiv_id else None
    chart_payloads = {
        "year_trend": _build_year_trend(citing_papers),
        "institution_distribution": institution_distribution,
        "source_map": institution_distribution,
        "country_distribution": country_distribution,
        "scholar_distribution": _build_scholar_distribution(scholar_labels),
        "h_index_distribution": _build_h_index_distribution(author_profiles),
        "sentiment_distribution": dict(sentiment_summary.label_counts),
    }
    summary = {
        "target_title": target_paper.title,
        "target_title_zh": target_title_zh,
        "target_doi": target_paper.doi,
        "target_arxiv_id": target_arxiv_id,
        "target_arxiv_url": target_arxiv_url,
        "citation_count": len(citing_papers),
        "heavyweight_candidates": author_summary.heavyweight_candidates,
        "high_impact_candidates": author_summary.high_impact_candidates,
        "weak_signal_candidates": author_summary.weak_signal_candidates,
        "author_profile_breakdown": provenance["author_profile_breakdown"],
        "context_found": sentiment_summary.context_found,
        "unknown_sentiments": sentiment_summary.unknown_count,
        "partial_failure": provenance["partial_failure"],
        "source_trace_count": provenance["source_trace_count"],
        "source_trace_sources": provenance["source_trace_sources"],
        "executive_summary": _build_executive_summary(
            citing_papers,
            scholar_labels,
            sentiment_summary,
            chart_payloads,
            provenance,
            summary_builder=executive_summary_builder,
        ),
        "key_findings": _build_key_findings(citing_papers, scholar_labels, sentiment_summary, provenance),
        "top_scholars": _build_top_scholars(author_profiles, scholar_labels),
        "representative_contexts": _build_representative_contexts(citation_contexts, citing_papers),
        "manual_attention_items": _build_manual_attention_items(citation_contexts, scholar_labels, provenance),
        "author_identity_skipped_papers": list(author_identity_skipped_papers or []),
        "pdf_export_status": "pending",
    }

    json_path = report_dir / "report.json"
    html_path = report_dir / "report.html"
    pdf_path = report_dir / "report.pdf"
    payload = {
        "summary": summary,
        "charts": chart_payloads,
        "provenance": provenance,
        "contexts": [_serialize_context(context) for context in citation_contexts],
    }

    export_paths = {
        "html": str(html_path),
        "json": str(json_path),
    }
    try:
        render_pdf_report(payload, pdf_path)
    except Exception as exc:  # PDF export is not allowed to break HTML/JSON.
        reason = f"pdf_export_unavailable:{exc.__class__.__name__}:{exc}"
        provenance["pdf_export_status"] = "unavailable"
        provenance["report_warnings"].append(reason)  # type: ignore[union-attr]
        summary["pdf_export_status"] = "unavailable"
        get_runtime_logger().warn("report.pdf", "PDF 导出不可用，HTML/JSON 已生成", reason=reason)
    else:
        provenance["pdf_export_status"] = "generated"
        summary["pdf_export_status"] = "generated"
        export_paths["pdf"] = str(pdf_path)

    json_path.write_text(
        json.dumps(
            payload,
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )
    html_path.write_text(
        _render_html(target_paper, summary, chart_payloads, provenance, citation_contexts),
        encoding="utf-8",
    )

    return ReportArtifact(
        report_id=safe_report_id,
        target_paper_id=target_paper.canonical_id or target_paper.doi or safe_report_id,
        summary=summary,
        charts=chart_payloads,
        export_paths=export_paths,
    )


def attach_report_artifact_to_state(state: AnalysisState, artifact: ReportArtifact) -> AnalysisState:
    """Store generated report paths and mark the analyzer state as report-ready."""
    state["report_artifact"] = artifact  # type: ignore[assignment]
    state["status"] = "report_generated"
    return state


def _build_year_trend(citing_papers: Iterable[CitingPaper]) -> dict[str, int]:
    """Count citing papers per publication year for trend visualization."""
    counts = Counter(str(paper.year) for paper in citing_papers if paper.year is not None)
    return dict(sorted(counts.items()))


def _build_institution_distribution(author_profiles: Iterable[AuthorProfile]) -> dict[str, int]:
    """Count first-known author affiliations for institution distribution charts."""
    counts = Counter()
    for profile in author_profiles:
        if profile.affiliations:
            counts[profile.affiliations[0]] += 1
    return dict(sorted(counts.items()))


def _build_country_distribution(
    author_profiles: Iterable[AuthorProfile],
    resolver: CountryResolverProtocol | None = None,
) -> tuple[dict[str, int], list[dict[str, object]]]:
    """Resolve author country buckets from explicit country hints, institutions, and LLM inference."""
    active_resolver = resolver or HybridCountryResolver()
    profiles = list(author_profiles)
    author_inputs = [
        AuthorCountryInput(
            author_id=profile.source_ids.get("openalex") or profile.author_id,
            author_name=profile.name,
            institutions=tuple(institution for institution in profile.affiliations if institution.strip()),
            country_hints=tuple(country for country in profile.countries if country.strip()),
        )
        for profile in profiles
    ]
    if hasattr(active_resolver, "resolve_author_many"):
        resolved_by_author = active_resolver.resolve_author_many(author_inputs)  # type: ignore[attr-defined]
    else:
        resolved_by_author = {}
        institutions = [
            profile.affiliations[0]
            for profile in profiles
            if profile.affiliations and profile.affiliations[0].strip()
        ]
        resolved_by_institution = active_resolver.resolve_many(list(dict.fromkeys(institutions))) if hasattr(active_resolver, "resolve_many") else {}  # type: ignore[attr-defined]
    counts = Counter()
    trace = []
    for profile, author_input in zip(profiles, author_inputs):
        result = resolved_by_author.get(author_input.author_id)
        if result is None and "resolved_by_institution" in locals():
            institution = profile.affiliations[0] if profile.affiliations else ""
            result = resolved_by_institution.get(institution) if institution else None
            if result is None and institution:
                result = active_resolver.resolve(institution)
                resolved_by_institution[institution] = result
        if result is None:
            counts["Unknown"] += 1
            trace.append(
                {
                    "author_id": author_input.author_id,
                    "author_name": author_input.author_name,
                    "institution": "",
                    "country": "Unknown",
                    "country_code": None,
                    "confidence": "low",
                    "method": "missing",
                    "evidence": f"{profile.name}: 缺少明确国家代码、国家名和机构字段。",
                    "needs_review": True,
                    "basis": "unknown",
                    "is_inferred": False,
                    "country_hints": list(author_input.country_hints),
                }
            )
            continue
        counts[result.country] += 1
        trace.append(result.to_dict())
    return dict(sorted(counts.items())), trace


def _translate_target_title(
    title: str | None,
    title_translator: Callable[[str], str | None] | None,
    provenance: dict[str, object],
) -> str | None:
    """Translate the target title without letting LLM failures break reports."""
    if not title or not title_translator:
        return None
    try:
        return title_translator(title)
    except Exception as exc:
        reason = f"title_translation_unavailable:{exc.__class__.__name__}:{exc}"
        report_warnings = provenance.setdefault("report_warnings", [])
        if isinstance(report_warnings, list):
            report_warnings.append(reason)
        get_runtime_logger().warn("report.title_translation", "中文标题翻译不可用，报告继续生成", reason=reason)
        return None


def _target_arxiv_id(target_paper: TargetPaper) -> str | None:
    """Return the normalized arXiv identifier when the target paper has one."""
    candidates = [
        target_paper.source_ids.get("arxiv") if target_paper.source_ids else None,
        target_paper.source_ids.get("arxiv_id") if target_paper.source_ids else None,
        target_paper.paper_query if target_paper.paper_query_type == "arxiv" else None,
        target_paper.canonical_id,
    ]
    for candidate in candidates:
        if not candidate:
            continue
        match = re.search(r"(?P<id>\d{4}\.\d{4,5}(?:v\d+)?)", str(candidate), re.IGNORECASE)
        if match:
            return match.group("id")
    return None


def _build_scholar_distribution(labels: Iterable[ScholarLabel]) -> dict[str, int]:
    """Count scholar labels for the author-impact overview chart."""
    counts = Counter(label.label for label in labels)
    return dict(sorted(counts.items()))


def _build_h_index_distribution(author_profiles: Iterable[AuthorProfile]) -> dict[str, int]:
    """Bucket authors by h-index for a reader-facing distribution chart."""
    counts = Counter({label: 0 for label, _, _ in H_INDEX_BUCKETS})
    for profile in author_profiles:
        if profile.h_index is None:
            continue
        h_index = int(profile.h_index)
        for label, lower, upper in H_INDEX_BUCKETS:
            if h_index >= lower and (upper is None or h_index <= upper):
                counts[label] += 1
                break
    return dict(counts)


def _build_author_profile_breakdown(
    author_profiles: Iterable[AuthorProfile],
    scholar_labels: Iterable[ScholarLabel],
) -> dict[str, int]:
    """Classify author profiles by evidence completeness and impact rules."""
    labels_by_author = {label.author_id: label for label in scholar_labels}
    counts = Counter(
        {
            "heavyweight": 0,
            "high_impact": 0,
            "complete_regular": 0,
            "complete_missing_metric": 0,
            "weak_work_authorship": 0,
            "profile_total": 0,
            "complete_profile_total": 0,
        }
    )
    for profile in author_profiles:
        counts["profile_total"] += 1
        label = labels_by_author.get(profile.author_id)
        has_complete_profile = "openalex_author_id" in set(profile.evidence_sources)
        if has_complete_profile:
            counts["complete_profile_total"] += 1
        if label and label.label == "heavyweight_candidate":
            counts["heavyweight"] += 1
        elif label and label.label == "high_impact_candidate":
            counts["high_impact"] += 1
        elif has_complete_profile and profile.h_index is not None:
            counts["complete_regular"] += 1
        elif has_complete_profile:
            counts["complete_missing_metric"] += 1
        else:
            counts["weak_work_authorship"] += 1
    return dict(counts)


def _build_key_findings(
    citing_papers: list[CitingPaper],
    scholar_labels: list[ScholarLabel],
    sentiment_summary: SentimentSummary,
    provenance: dict[str, object],
) -> list[str]:
    """Create short findings that summarize citation volume, authors, and sentiment."""
    findings = [
        f"共识别 {len(citing_papers)} 篇施引论文。",
        f"重量级学者候选 {sum(1 for label in scholar_labels if label.label == 'heavyweight_candidate')} 位。",
        f"已分类引用 {sentiment_summary.classified_count} 条，无法判断 {sentiment_summary.unknown_count} 条。",
    ]
    if provenance["partial_failure"]:
        findings.append("上游抓取或解析存在部分失败，结论需结合注意事项复核。")
    breakdown = provenance.get("author_profile_breakdown")
    if isinstance(breakdown, dict):
        findings.append(
            "作者画像分类："
            f"完整画像 {breakdown.get('complete_profile_total', 0)} 位，"
            f"弱画像 {breakdown.get('weak_work_authorship', 0)} 位。"
        )
    skipped_authors = provenance.get("author_identity_skipped_papers")
    if isinstance(skipped_authors, list) and skipped_authors:
        findings.append(f"作者画像跳过 {len(skipped_authors)} 篇施引论文，以避免低置信作者错配。")
    return findings


def _build_executive_summary(
    citing_papers: list[CitingPaper],
    scholar_labels: list[ScholarLabel],
    sentiment_summary: SentimentSummary,
    charts: dict[str, object],
    provenance: dict[str, object],
    *,
    summary_builder: ExecutiveSummaryBuilder | None = None,
) -> list[str]:
    """Create reader-facing summary bullets with LLM wording and safe fallback."""
    facts = _build_executive_summary_facts(citing_papers, scholar_labels, sentiment_summary, charts, provenance)
    if summary_builder is not None:
        try:
            bullets = _sanitize_executive_summary_bullets(summary_builder(facts))
        except Exception as exc:
            _append_report_warning(provenance, f"executive_summary_builder_failed:{exc.__class__.__name__}:{exc}")
        else:
            if bullets:
                provenance["executive_summary_source"] = "custom_builder"
                return bullets

    try:
        bullets = _build_llm_executive_summary(facts)
    except Exception as exc:
        _append_report_warning(provenance, f"executive_summary_llm_failed:{exc.__class__.__name__}:{exc}")
    else:
        bullets = _sanitize_executive_summary_bullets(bullets)
        if bullets:
            provenance["executive_summary_source"] = "llm"
            return bullets

    provenance["executive_summary_source"] = "fallback"
    return _build_fallback_executive_summary(facts)


def _build_executive_summary_facts(
    citing_papers: list[CitingPaper],
    scholar_labels: list[ScholarLabel],
    sentiment_summary: SentimentSummary,
    charts: dict[str, object],
    provenance: dict[str, object],
) -> dict[str, object]:
    """Collect the exact numeric facts allowed in the executive summary prompt."""
    year_trend = _coerce_int_map(charts.get("year_trend"))
    country_distribution = _coerce_int_map(charts.get("country_distribution"))
    author_profile_breakdown = provenance.get("author_profile_breakdown")
    author_profile_breakdown = author_profile_breakdown if isinstance(author_profile_breakdown, dict) else {}
    total_authors = _as_int(author_profile_breakdown.get("profile_total"))
    unknown_country_count = country_distribution.get("Unknown", 0)
    located_country_count = max(total_authors - unknown_country_count, 0) if total_authors else 0
    heavyweight_count = sum(1 for label in scholar_labels if label.label == "heavyweight_candidate")
    high_impact_count = sum(1 for label in scholar_labels if label.label == "high_impact_candidate")
    important_scholars = sum(
        1
        for label in scholar_labels
        if label.label in {"heavyweight_candidate", "high_impact_candidate"}
    )
    peak_year = None
    peak_count = None
    if year_trend:
        peak_year, peak_count = max(year_trend.items(), key=lambda item: item[1])

    return {
        "citation_count": len(citing_papers),
        "classified_sentiments": sentiment_summary.classified_count,
        "unknown_sentiments": sentiment_summary.unknown_count,
        "sentiment_counts": dict(sentiment_summary.label_counts),
        "heavyweight_candidates": heavyweight_count,
        "high_impact_candidates": high_impact_count,
        "important_scholar_candidates": important_scholars,
        "peak_year": peak_year,
        "peak_count": peak_count,
        "dedup_author_count": total_authors,
        "country_located_count": located_country_count,
        "country_unknown_count": unknown_country_count,
        "complete_author_profiles": _as_int(author_profile_breakdown.get("complete_profile_total")),
        "skipped_author_papers": len(provenance.get("author_identity_skipped_papers", []))
        if isinstance(provenance.get("author_identity_skipped_papers"), list)
        else 0,
        "partial_failure": bool(provenance.get("partial_failure")),
        "state_error_count": len(provenance.get("state_errors", []))
        if isinstance(provenance.get("state_errors"), list)
        else 0,
        "fetch_note_count": len(provenance.get("fetch_notes", []))
        if isinstance(provenance.get("fetch_notes"), list)
        else 0,
    }


def _build_llm_executive_summary(facts: dict[str, object]) -> list[str]:
    """Ask the configured LLM to write concise Chinese report-summary bullets."""
    from apps.analyzer.config import build_llm, invoke_llm_with_retry

    structured_llm = build_llm().with_structured_output(ExecutiveSummaryModel, method="function_calling")
    prompt = (
        "你正在为中文论文被引分析报告撰写“分析摘要”。"
        "只能使用用户提供的事实和数字，不能新增、猜测或夸大结论。"
        "输出 4 到 5 条 bullets，每条必须是一句完整、自然、面向读者的中文。"
        "必须把字段转写成自然语言：citation_count 写成“施引文献数量”，dedup_author_count 写成“去重施引作者数”。"
        "绝对不要在摘要中出现 citation_count、dedup_author_count、unknown_sentiments、classified_sentiments 等英文变量名。"
        "未知情感的原因应概括为“全文不可用、引用上下文未命中或证据不足”。"
        "必须说明重要学者候选是系统按 h-index 与施引频次规则筛选，不是人工确认。"
        "必须使用“重量级候选”这个术语，不要改写成“重磅学者候选”。"
        "国家/地区部分必须写成“已定位 X/Y 位作者，另有 Z 位缺少可定位国家信息”。"
        "不要写“Unknown 当前最多”，不要把 Unknown 当作国家/地区，不要出现 Data Quality、人工关注项、debug 等工程术语。"
        "不要使用 Markdown 加粗、编号或多余标题。"
    )
    result = invoke_llm_with_retry(
        structured_llm,
        [
            {"role": "system", "content": prompt},
            {"role": "user", "content": json.dumps(facts, ensure_ascii=False, indent=2)},
        ],
        "阶段7分析摘要生成",
    )
    return list(getattr(result, "bullets", []) or [])


def _build_fallback_executive_summary(facts: dict[str, object]) -> list[str]:
    """Generate stable Chinese summary bullets when LLM wording is unavailable."""
    citation_count = _as_int(facts.get("citation_count"))
    classified = _as_int(facts.get("classified_sentiments"))
    unknown = _as_int(facts.get("unknown_sentiments"))
    important = _as_int(facts.get("important_scholar_candidates"))
    heavyweight = _as_int(facts.get("heavyweight_candidates"))
    high_impact = _as_int(facts.get("high_impact_candidates"))
    total_authors = _as_int(facts.get("dedup_author_count"))
    located = _as_int(facts.get("country_located_count"))
    country_unknown = _as_int(facts.get("country_unknown_count"))
    summary = [
        f"本次共识别 {citation_count} 篇施引文献，形成当前可复核的数据快照。",
        f"在 {citation_count} 篇施引文献中，{classified} 篇已完成引用情感判断，{unknown} 篇因全文不可用、引用上下文未命中或证据不足暂未判断。",
        f"系统按 h-index 与施引频次筛出 {important} 位重要学者候选，其中重量级候选 {heavyweight} 位、高影响力候选 {high_impact} 位。",
    ]
    peak_year = facts.get("peak_year")
    peak_count = facts.get("peak_count")
    if peak_year and peak_count is not None:
        summary.append(f"施引年份峰值出现在 {peak_year} 年，共 {peak_count} 篇。")
    if total_authors:
        summary.append(f"本次共识别 {total_authors} 位去重施引作者，其中 {located} 位可定位国家/地区，另有 {country_unknown} 位缺少可定位国家信息。")
    return summary[:5]


def _sanitize_executive_summary_bullets(bullets: list[str]) -> list[str]:
    """Keep generated summary bullets concise and remove unsafe reader-facing phrasing."""
    cleaned: list[str] = []
    banned_fragments = (
        "Unknown 当前最多",
        "Data Quality",
        "人工关注项",
        "debug",
        "Debug",
        "citation_count",
        "dedup_author_count",
        "unknown_sentiments",
        "classified_sentiments",
        "重磅学者",
    )
    for bullet in bullets:
        text = re.sub(r"\s+", " ", str(bullet or "")).strip(" -•\t\r\n")
        if not text or any(fragment in text for fragment in banned_fragments):
            continue
        cleaned.append(text)
        if len(cleaned) >= 5:
            break
    return cleaned if len(cleaned) >= 4 else []


