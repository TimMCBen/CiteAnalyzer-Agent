def _build_quality_summary(summary: dict[str, object], provenance: dict[str, object]) -> dict[str, object]:
    """Summarize upstream failures and coverage gaps into a data-quality badge."""
    unknown = _as_int(summary.get("unknown_sentiments"))
    partial_failure = bool(summary.get("partial_failure"))
    fetch_notes = provenance.get("fetch_notes") if isinstance(provenance.get("fetch_notes"), list) else []
    state_errors = provenance.get("state_errors") if isinstance(provenance.get("state_errors"), list) else []
    low_confidence = provenance.get("low_confidence_labels")
    low_confidence_count = len(low_confidence) if isinstance(low_confidence, list) else 0
    skipped_author_identity = provenance.get("author_identity_skipped_papers")
    skipped_author_identity_count = len(skipped_author_identity) if isinstance(skipped_author_identity, list) else 0
    breakdown = provenance.get("author_profile_breakdown")
    breakdown = breakdown if isinstance(breakdown, dict) else {}
    weak_work_authorship = _as_int(breakdown.get("weak_work_authorship"))
    complete_profile_total = _as_int(breakdown.get("complete_profile_total"))
    complete_regular = _as_int(breakdown.get("complete_regular"))
    complete_missing_metric = _as_int(breakdown.get("complete_missing_metric"))

    if partial_failure or state_errors:
        level = "risk"
        label = "需复核"
        note = "存在上游部分失败或状态错误，结论需要结合详情复核。"
    elif unknown > 0 or fetch_notes or skipped_author_identity_count:
        level = "warning"
        label = "部分缺失"
        note = "存在未知情感、抓取提示或作者画像跳过记录，部分证据不足。"
    else:
        level = "ok"
        label = "可靠"
        note = "未发现阻塞性失败，报告可作为当前数据快照阅读。"

    return {
        "level": level,
        "label": label,
        "note": note,
        "items": [
            {"label": "未知情感", "value": unknown},
            {"label": "抓取提示", "value": len(fetch_notes)},
            {"label": "状态错误", "value": len(state_errors)},
            {"label": "完整画像作者", "value": complete_profile_total},
            {"label": "普通完整画像", "value": complete_regular},
            {"label": "完整画像缺指标", "value": complete_missing_metric},
            {"label": "弱画像作者", "value": weak_work_authorship},
            {"label": "作者跳过", "value": skipped_author_identity_count},
        ],
    }


def _render_year_fallback(year_trend: dict[str, object]) -> str:
    """Describe year data when a trend chart would be visually misleading."""
    fallback = year_trend.get("fallback")
    if not isinstance(fallback, dict) or not fallback:
        return "<p class='muted chart-fallback'>暂无年份数据。</p>"
    return "<ul class='data-list chart-fallback'>" + "".join(
        f"<li><span>{year}</span><strong>{count}</strong></li>"
        for year, count in fallback.items()
    ) + "</ul>"


def _year_trend_note(year_trend: dict[str, int], summary: dict[str, object]) -> str:
    """Explain whether the year chart represents a trend or a small-sample count."""
    if len(year_trend) >= 2:
        return "展示施引文献按年份分布，用于判断影响是否持续扩散。"
    if len(year_trend) == 1:
        year, count = next(iter(year_trend.items()))
        return f"当前样本集中在 {year} 年，共 {count} 篇施引文献。"
    return f"当前 {summary.get('citation_count', 0)} 篇施引文献缺少年份信息。"


def _country_map_note(country_map_payload: dict[str, object]) -> str:
    """Explain map coverage, including Unknown and unmapped country buckets."""
    unknown_count = _as_int(country_map_payload.get("unknownCount"))
    unmapped_items = country_map_payload.get("unmappedItems")
    unmapped_count = 0
    if isinstance(unmapped_items, list):
        unmapped_count = sum(_as_int(item.get("value")) for item in unmapped_items if isinstance(item, dict))
    notes = ["优先使用 OpenAlex 明确国家代码/国家名；缺少明确国家时，再由 LLM 根据作者名、OpenAlex ID 与机构信息判断国家/地区。"]
    if unknown_count:
        notes.append(f"Unknown={unknown_count} 不参与地图着色。")
    if unmapped_count:
        notes.append(f"未映射地区共 {unmapped_count} 条，保留在下方列表中。")
    return " ".join(notes)


def _coerce_int_map(value: object) -> dict[str, int]:
    """Normalize chart count mappings into string labels and integer counts."""
    if not isinstance(value, dict):
        return {}
    result = {}
    for key, raw_count in value.items():
        count = _as_int(raw_count)
        if count < 0:
            continue
        result[str(key)] = count
    return result


def _nonzero_items(items: dict[str, int]) -> list[tuple[str, int]]:
    return [
        (label, count)
        for label, count in sorted(items.items(), key=lambda item: (-item[1], item[0]))
        if count > 0
    ]


def _top_n_with_others(items: dict[str, int], limit: int) -> list[tuple[str, int]]:
    """Keep top chart categories and fold the rest into an Others bucket."""
    nonzero = _nonzero_items(items)
    if len(nonzero) <= limit:
        return nonzero
    top_items = nonzero[:limit]
    other_count = sum(count for _, count in nonzero[limit:])
    if other_count:
        top_items.append(("Others", other_count))
    return top_items


def _as_int(value: object) -> int:
    """Convert optional report metric values into safe integers."""
    try:
        return int(value)  # type: ignore[arg-type]
    except (TypeError, ValueError):
        return 0


def _json_for_script(value: object) -> str:
    return json.dumps(value, ensure_ascii=False).replace("</", "<\\/")


def _slugify(value: str) -> str:
    return "".join(ch.lower() if ch.isalnum() else "-" for ch in value).strip("-") or "report"
