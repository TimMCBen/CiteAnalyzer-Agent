def _display_reason(evidence_note: str | None) -> str:
    """Extract a human-readable report reason from raw pipeline evidence."""
    if not evidence_note:
        return "上下文证据不足，无法形成可靠判断。"
    reason = str(evidence_note).strip()
    if "llm_sentiment:" in reason:
        reason = reason.split("llm_sentiment:")[-1].strip()
    else:
        parts = [part.strip() for part in reason.split(";") if part.strip()]
        readable_parts = [
            part
            for part in parts
            if not re.match(r"^(matched_by_|no_text_available|context_too_short|grobid_|reference_)", part)
        ]
        reason = readable_parts[-1] if readable_parts else reason
    reason = re.sub(r"^(原因[:：]\s*)+", "", reason).strip()
    if not reason:
        return "上下文证据不足，无法形成可靠判断。"
    return reason


def _escape_html(value: object) -> str:
    """Escape report display values before placing them in HTML fragments."""
    return html.escape(str(value), quote=True)


